package ontology.conceptos;


public class Fase extends Concepto {
	private int numero;
    private String nombre;
	
	public Fase() {}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int valor) {
		this.numero = numero;
	}
    public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}
